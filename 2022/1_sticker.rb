def rot_x(x, string)
  # quick and dirty, assume all caps A-Z
  # "A".ord == 65
  new_string = ""
  string.each_char do |char|
    new_string << (char.ord - 65 + x).modulo(26).+(65).chr
  end
  new_string
end

